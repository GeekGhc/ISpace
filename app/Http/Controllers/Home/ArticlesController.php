<?php

namespace App\Http\Controllers\Home;

use Auth;
use App\Article;
use App\Comment;
use App\Events\ArticleView;
use App\Favorite;
use App\Http\Requests\ArticleStoreRequest;
use App\Markdown\Markdown;
use App\Tag;
use App\Timeline;
use EndaEditor;
use Illuminate\Http\Request;
use App\Http\Controllers\Controller;

class ArticlesController extends Controller
{
    protected $markdown;
    public function __construct(Markdown $markdown)
    {
        $this->markdown = $markdown;
        $this->middleware('auth',['except'=>['index','show']]);
    }

    /**
     * Display a listing of the resource.
     *
     * @return \Illuminate\Http\Response
     */
    public function index()
    {
        $articles = Article::with('user')->latest()->paginate(10);
        return view('articles.index',compact('articles'));
    }

    /**
     * Show the form for creating a new resource.
     *
     * @return \Illuminate\Http\Response
     */
    public function create()
    {
        $tags = Tag::pluck('name','id');
        return view('articles.create',compact('tags'));
    }

    /**
     * Store a newly created resource in storage.
     */
    public function store(ArticleStoreRequest $request)
    {
        $data = [
            'user_id'=>Auth::user()->id,
            'last_user_id'=>Auth::user()->id,
            'html_body'=>$this->markdown->markdown($request->get('body'))
        ];

        //保存用户数据
        $article = Article::create(array_merge($request->all(), $data));
        $timeLine = Timeline::create([
            'user_id'=>Auth::user()->id,
            'operation_id'=>$article->id,
            'operation_type'=>'comment',
            'operation_class'=>'App\Article',
            'operation_text'=>'发表文章',
            'operation_icon'=>'fa-coffee'
        ]);
        $article->tags()->attach($request->get('tag_list'));
        return redirect('/article');
    }

    /**
     * Display the specified resource.
     */
    public function show($id)
    {
        $article = Article::with('user')->findOrFail($id);
        event(new ArticleView($article));
        $prevArticle = Article::find($article->getPrevArticleId($id));
        $nextArticle = Article::find($article->getNextArticleId($id));
        $favorite = Favorite::where('favoriteable_type','App\Article')->where('favoriteable_id',$article->id)->first();
        if(\Auth::check()){
            if($favorite){
                $isFavorite = \Auth::user()->id==$favorite->user_id;
                if(!$isFavorite){$isFavorite = 0;}
            }else{
                $isFavorite = 0;
            }
        }else{
            $isFavorite = 2;
        }

        $comments = Comment::with('user')->with('to_user')->where('commentable_type','App\Article')->where('commentable_id',$id)->get();
        return view('articles.show',compact('article','prevArticle','nextArticle','isFavorite','comments'));
    }

    /**
     * Show the form for editing the specified resource.
     *
     * @param  int  $id
     * @return \Illuminate\Http\Response
     */
    public function edit($id)
    {
        $tags = Tag::pluck('name','id');
        $article = Article::with('user')->findOrFail($id);
        if(\Auth::user()->id !== $article->user->id){
            return redirect('/');
        }
        return view('articles.edit', compact('article','tags'));
    }

    //更新文章内容
    public function update(Request $request, $id)
    {
        $article = Article::findOrFail($id);
        $data = [
            'html_body'=>$this->markdown->markdown($request->get('body'))
        ];
        $article->update(array_merge($request->all(), $data));
        $article->tags()->sync($request->get('tag_list'));
        return redirect()->action('Home\ArticlesController@index');
    }

    /**
     * Remove the specified resource from storage.
     *
     * @param  int  $id
     * @return \Illuminate\Http\Response
     */
    public function destroy($id)
    {
        //
    }
}
